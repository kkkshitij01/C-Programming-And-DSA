#include<stdio.h>

// program to calculate the Area of rectangle

int main ()
{
    int length, breadth;
    printf("Enter the Length of Rectangle\n");
    scanf("%d",&length);
    printf("Enter the Breadth of Rectangle\n");
    scanf("%d",&breadth);

    printf("The area of Rectangle is \n%d\n ", length * breadth);
    return 0;


}
